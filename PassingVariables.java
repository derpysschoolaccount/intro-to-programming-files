public class PassingVariables {
    public static void myAge(int age) {
        System.out.println("My age is " + age);
    }
    public static void myMiddle(String middle) {
        System.out.println("My middle initial is " + middle);
    }
    public static void myName(String name) {
		System.out.println("My name is " + name);
	}


    public static void main(String[] args) {
        int age = 12951724;
        String middle = "idk";
        String name = "wnhvjeiwejuhfvbgtuhtyb";

        myAge(age);
        myMiddle(middle);
        myName(name);

        age = 65873;
        middle = "jhygtgb";
        name = "mkijgtu8rq70";

        myAge(age);
        myMiddle(middle);
        myName(name);

        age = 796756;
        middle = "ijuyt";
        name = "oibjuhygb";

        myAge(age);
        myMiddle(middle);
        myName(name);
    }
}
